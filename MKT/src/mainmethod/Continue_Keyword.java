package mainmethod;

public class Continue_Keyword {

	public static void main(String[] args) 
	
	{
		for(int i=0; i<=5; i++)
		{
			
			if(i==4) 
			{
				continue;
			}
			System.out.println(i);
		}
		System.out.println("Next Iteration");
	for(int i=0; i<=10;i++)
	{
		if(i>=5) {
			continue;
		}
		
		System.out.println(i);
	}
	}

}
