package mainmethod;

public class Multiple_Static_method 
{
	
	static void add() {
		System.out.println("Addition");
	}
	
	static void sub() {
		System.out.println("Sub");
	}

	static void mul() {
		System.out.println("Mul");
	}
	
	public static void main(String[] args)
	
	{
		System.out.println("Main Method");
		add();
		sub();
		mul();

	}

}
